# Create GitHub repository (you need to do this manually first at github.com)
# After creating repo at https://github.com/new, run:

# Add remote origin (replace YOUR-USERNAME)
git remote add origin https://github.com/YOUR-USERNAME/seeu-dating.git

# Rename branch to main
git branch -M main

# First commit
git add .
git commit -m "Initial commit: See U Dating App"

# Push to GitHub
git push -u origin main